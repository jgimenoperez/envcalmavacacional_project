// Función para obtener el ID del artículo de la URL
function getArticleIdFromURL() {
    const params = new URLSearchParams(window.location.search);
    return parseInt(params.get('id'));
}

// Función para obtener un artículo por ID
function getArticleById(id) {
    return articlesData.find(article => article.id === id);
}

// Función para renderizar el contenido del artículo
function renderArticleContent(article) {
    const container = document.getElementById('articleBody');

    let contentHTML = '';

    article.content.forEach((section, index) => {
        if (section.type === 'paragraph') {
            contentHTML += `
                <h3 class="article-section-title">${section.title}</h3>
                <p class="article-paragraph">${section.text}</p>
            `;
        } else if (section.type === 'image') {
            const colors = ['#D4E8E0', '#E8E0D4', '#D4D8E8'];
            const color = colors[index % colors.length];
            contentHTML += `<div class="article-image" style="background-color: ${color};"></div>`;
        } else if (section.type === 'tips') {
            contentHTML += `
                <div class="article-tip-section">
                    <p class="article-tip-title">Consejos útiles</p>
                    ${section.tips.map(tip => `<p class="article-tip-item">${tip}</p>`).join('')}
                </div>
            `;
        }
    });

    container.innerHTML = contentHTML;
}

// Función para renderizar artículos relacionados
function renderRelatedArticles(currentId) {
    const container = document.getElementById('relatedArticlesContainer');
    const related = articlesData.filter(article => article.id !== currentId).slice(0, 3);

    container.innerHTML = related.map(article => `
        <article class="related-article">
            <div class="related-article-image"></div>
            <div class="related-article-content">
                <span class="related-article-category">${article.category}</span>
                <h4 class="related-article-title">${article.title}</h4>
                <p class="related-article-excerpt">${article.excerpt}</p>
                <a href="article.html?id=${article.id}" class="related-article-link">Leer más →</a>
            </div>
        </article>
    `).join('');
}

// Función para cargar y renderizar el artículo
function loadArticle() {
    const articleId = getArticleIdFromURL();
    const article = getArticleById(articleId);

    if (!article) {
        document.getElementById('articleBody').innerHTML = '<p>Artículo no encontrado.</p>';
        return;
    }

    // Renderizar meta información
    document.getElementById('articleCategory').textContent = article.category.toUpperCase();
    document.getElementById('articleTitle').textContent = article.title;
    document.getElementById('articleDate').textContent = article.date;
    document.getElementById('articleAuthor').textContent = article.author;

    // Renderizar contenido
    renderArticleContent(article);

    // Renderizar artículos relacionados
    renderRelatedArticles(article.id);
}

// Inicializar cuando el DOM esté listo
document.addEventListener('DOMContentLoaded', loadArticle);
